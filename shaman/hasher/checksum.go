import (
	"crypto/sha256"
	"fmt"
)

// Checksum computes the SHA256 sum of the data and returns it as hexadecimal string.
func Checksum(data []byte) string {
	hasher := sha256.New()
	hasher.Write(data)
	hashsum := hasher.Sum(nil)
	return fmt.Sprintf("%x", hashsum)
}
